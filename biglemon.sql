CREATE schema biglemon;
use biglemon;

CREATE TABLE Customer(CustomerID INT PRIMARY KEY, FullName VARCHAR(255) NOT NULL, Email VARCHAR(255) NOT NULL);

CREATE TABLE MenuItem(MenuItemsID INT PRIMARY KEY, CourseName VARCHAR(255) NOT NULL, StarterName VARCHAR(255) NOT NULL, DessertName VARCHAR(255) NOT NULL);
ALTER TABLE MenuItem RENAME COLUMN MenuItemsID TO MenuItemID;

CREATE TABLE Menus(MenuID INT NOT NULL PRIMARY KEY, MenuName VARCHAR(255) NOT NULL, Cuisine VARCHAR(255) NOT NULL, MenuItemID INT NOT NULL, FOREIGN KEY (MenuItemID) REFERENCES MenuItem(MenuItemID) ON DELETE cascade ON UPDATE cascade);

CREATE TABLE Orders(OrderID INT NOT NULL PRIMARY KEY, OrderDate DATE NOT NULL, CustomerID INT NOT NULL, MenuID INT NOT NULL, Quantity INT NOT NULL, TotalCost DECIMAL(10,2) NOT NULL, FOREIGN KEY (CustomerID) REFERENCES Customer(CustomerID) ON DELETE cascade ON UPDATE cascade,
FOREIGN KEY (MenuID) REFERENCES Menus(MenuID) ON DELETE cascade ON UPDATE cascade);

CREATE TABLE Delivery(DeliveryID INT NOT NULL PRIMARY KEY, DeliveryDate DATE NOT NULL, DeliveryStatus VARCHAR(255) NOT NULL);

CREATE TABLE Staff(StaffID INT NOT NULL PRIMARY KEY, Role VARCHAR(255) NOT NULL, Salary DECIMAL(10,2) NOT NULL);
ALTER TABLE Staff MODIFY Salary DECIMAL(10,2);

CREATE TABLE Booking(BookingID INT NOT NULL PRIMARY KEY, TableNumber INT NOT NULL, BookingDate DATE NOT NULL, OrderID INT NOT NULL, FOREIGN KEY(OrderID) REFERENCES Orders(OrderID) ON DELETE cascade ON UPDATE cascade);


